public class Game {
	String type;
	String description;
	String gameName;
	double cost;
	
	public String getType() {
		return type;
	}
	
	public String getDescription() {
		return description;
	}
	
	public String getGameName() {
		return gameName;
	}

	public double cost() {
		return cost;
	}
}
